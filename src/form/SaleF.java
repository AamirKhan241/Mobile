    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Sales_Report s=new Sales_Report();
        
            s.setCustomer_name(name.getText());
            s.setCustomer_contact(contact.getText());
            s.setMobile_id(String.valueOf(m.getMobile_id()));
            s.setQuantity(quantity.getText());
            s.setGst(GST.getText());
            s.setAmount(amount.getText());
            
            m.setStock_avaiable(String.valueOf(Integer.valueOf(m.getStock_avaiable())-Integer.valueOf(quantity.getText())));
            if(mdao.update(m)){
                if(sdao.insert(s)){
                    JOptionPane.showMessageDialog(this, "SOLED");

                    Home h=new Home();
                    h.setVisible(true);
                    dispose();
                }else{
                    JOptionPane.showMessageDialog(this, "Sale can't be generate");
                }
            }else{
                JOptionPane.showMessageDialog(this, "Sale can't be generate");
            }
    }//GEN-LAST:event_jButton1ActionPerformed

