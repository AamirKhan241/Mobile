    private void manufacturerFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_manufacturerFocusLost
        list=mdao.display(manufacturer.getSelectedItem());
        for (Mobiles mobiles : list) {
            model.add(mobiles.getModel_no());
        }
    }//GEN-LAST:event_manufacturerFocusLost

    private void modelFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_modelFocusLost
        m=mdao.displayM(model.getSelectedItem(),manufacturer.getSelectedItem());
        price.setText(m.getUnit_price());
        quantity.setText(m.getStock_avaiable());
    }//GEN-LAST:event_modelFocusLost

    private void GSTFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_GSTFocusLost
        amount.setText(String.valueOf(Math.round((Integer.valueOf(price.getText()) * Integer.valueOf(quantity.getText()))
                *  Double.valueOf("1."+GST.getText()))));
    }//GEN-LAST:event_GSTFocusLost

